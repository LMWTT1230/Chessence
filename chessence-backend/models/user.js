import mongoose from "mongoose";

const UserSchema = new mongoose.Schema(
  {
    fistName: {
      type: String,
      required: true,
      trim: true,
    },
    lastName: {
      type: String,
      required: true,
      trim: true,
    },
    username: {
      type: String,
      required: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
    },
    
  },
  { collection: "user" }
);

export default mongoose.model("User", UserSchema);
